        var _currentSlide = 'slide-1';
        $(document).ready(function(){

            startGame();

            $('body').on('click', '.action-btn', function(e){

                let _nextSlide = $(this).attr('rel');
                switchSlide(_nextSlide);
            })
        });

        /**
        * this is just a helper function while developing so I don't start each time from the first slide
        **/
        function startGame(){
            _hash = location.hash;
            _nextSlide = _hash.length? _hash.slice(1) : _currentSlide;
            switchSlide(_nextSlide);
        }

        /**
        * the logic is as follows. All slides except the current one have opacity: 0 and display: none.
        * the animation first reduces the current slides opacity to 0 and then hides it (display: none).
        * The second step is to first switch the display on the new element and than animate the opacity to 100%.
        * This sequence of events create the fade in and out between slides.
        **/
        function switchSlide (_nextSlide) {
            console.log(_currentSlide, _nextSlide);
            $('.'+_currentSlide).animate({opacity:0}, {duration: 200, complete: function(){
                    $(this).hide();
                    $('.'+_nextSlide).show().animate({opacity:100}, 200);
                }
            });
            _currentSlide = _nextSlide;
        }
    </script>
